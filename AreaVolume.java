package PG2020;
public class AreaVolume
{

	public double areaCircle(double r)
	{
		System.out.println("Area of Circle: " + (3.14 * r * r));
		System.out.println("");
		return 0;
	}
	public double areaTriangle(double b, double h)
	{
		System.out.println("Area of Triangle: " + (0.5 * b * h));
		System.out.println("");
		return 0;
	}
	public double areaRectangle(double l, double b)
	{
		System.out.println("Addition: " + (l * b));
		System.out.println("");
		return 0;
	}
	public double areaSphere(double r)
	{
		System.out.println("Addition: " + (4 * 3.14 * r * r));
		System.out.println("");
		return 0;
	}
	public double volumeCircle(double r)
	{
		System.out.println("Perimeter of Circle: " + (2 * 3.14 * r));
		System.out.println("");
		return 0;
	}
	public double volumeSphere(double r)
	{
		System.out.println("Addition: " + (1.33 * 3.14 * r * r * r));
		System.out.println("");
		return 0;
	}
}